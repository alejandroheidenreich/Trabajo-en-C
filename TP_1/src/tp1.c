#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include "funciones.h"

int main(void) {
	int opcion;
	int flagMenuA = 1;
	int flagMenuB = 1;
	int flagOp = 1;
	float a,b,suma,resta,multiplicacion,division;
	int flagDivision;
	long int factorialA,factorialB;

	setbuf(stdout, NULL);

	do {

		if (flagMenuA && flagMenuB){
			printf("1. Ingresar 1er operando.\n");
			printf("2. Ingresar 2do operando.\n");
		}
		else{
			if(flagMenuA){
			printf("1. Ingresar 1er operando:\n");
			printf("2. Ingresar 2er operando: (B=%.3f)\n",b);
			}
			else{
				if(flagMenuB){
					printf("1. Ingresar 1er operando: (A=%.3f)\n",a);
					printf("2. Ingresar 2do operando.\n");
				}
				else{
					printf("1. Ingresar 1er operando: (A=%.3f)\n",a);
					printf("2. Ingresar 2er operando: (B=%.3f)\n",b);
				}
			}
		}

		printf("3. Calcular todas las operaciones.\n");
		printf("4. Informar resultados.\n");
		printf("5. Salir.\n");
		printf("Elija una opcion:\n");
		fflush(stdin);
		scanf("%d", &opcion);
		while(opcion < 1 || opcion > 5){
			printf("Elija una opcion valida: (1-5)\n");
			fflush(stdin);
			scanf("%d", &opcion);
		}
		switch(opcion){
		case 1:
			a = PedirNumero("Ingrese 1er operando: \n", "Error: Ingrese un operando valido: \n");
			flagMenuA = 0;
			break;
		case 2:
			b = PedirNumero("Ingrese 2do operando: \n", "Error: Ingrese un operando valido: \n");
			flagMenuB = 0;
			break;
		case 3:
			if (flagMenuA || flagMenuB){
				printf("Antes de realizar las operaciones, se deben ingresar los 2 operandos.\n");
			}
			else{
				suma = Suma(a,b);
				resta = Resta(a,b);
				flagDivision = Division(a,b,&division);
				multiplicacion = Multiplicacion(a,b);
				factorialA = Factorial(a);
				factorialB = Factorial(b);
				flagOp = 0;
				printf("Operaciones finalizadas.\n");
			}
			break;
		case 4:
			if (flagOp){
				printf("Antes de mostrar los resultados, se deben estar las operaciones finalizadas.\n");
			}
			else{
				printf("a) El resultado de A+B es: %.3f\n",suma);
				printf("b) El resultado de A-B es: %.3f\n",resta);
				if(flagDivision==0){
					printf("c) El resultado de A/B es: %.3f\n",division);
				}
				else{
					printf("c) No es posible dividir por cero\n");
				}
				printf("d) El resultado de A*B es: %.3f\n",multiplicacion);
				if (factorialA == 0){
					printf("e) El factorial de A no se puede calcular ");
				}
				else{
					printf("e) El factorial de A es: %ld ", factorialA);
				}
				if (factorialB == 0){
					printf("y El factorial de B no se puede calcular\n");
				}
				else{
					printf("y El factorial de B es: %ld\n", factorialB);
				}
			}
			break;
		default:
			printf("Cerrando calculadora. Chau!");
		}

	}while(opcion !=5);

//
	return EXIT_SUCCESS;
}

